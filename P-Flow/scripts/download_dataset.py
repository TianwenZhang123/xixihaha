#!/usr/bin/env python3
"""
Download MovieGenVideoBench full dataset from HuggingFace, then filter to 200 selected samples.

MovieGenVideoBench is hosted on HuggingFace:
    https://huggingface.co/datasets/meta-ai-for-media-research/movie_gen_video_bench

The dataset contains 1003 reference videos generated by Meta's MovieGen model.
We download the full dataset, then filter by selected_ids.txt to get our 200 samples.

Prerequisites:
    pip install huggingface_hub

Usage:
    # Download full dataset and filter to 200 (recommended)
    python scripts/download_dataset.py --download-hf

    # Specify custom output directory
    python scripts/download_dataset.py --download-hf --output-dir /root/autodl-tmp/data/video-200/water_mark_out

    # If you already have the full dataset downloaded, just filter from it
    python scripts/download_dataset.py --source-dir /path/to/full_dataset/videos

    # Use symlinks instead of copying (saves disk space)
    python scripts/download_dataset.py --source-dir /path/to/full_dataset/videos --symlink
"""

import argparse
import shutil
import sys
from pathlib import Path


def parse_args():
    parser = argparse.ArgumentParser(
        description="Download MovieGenVideoBench and filter to 200 selected samples"
    )
    parser.add_argument(
        "--output-dir", type=Path,
        default=Path("/root/autodl-tmp/data/video-200/water_mark_out"),
        help="Where to put the filtered 200 videos"
    )
    parser.add_argument(
        "--source-dir", type=Path, default=None,
        help="If you already have the full dataset videos locally, filter from here"
    )
    parser.add_argument(
        "--ids-file", type=Path,
        default=Path(__file__).parent.parent / "data" / "selected_ids.txt",
        help="File with selected video IDs (one per line)"
    )
    parser.add_argument(
        "--symlink", action="store_true",
        help="Create symlinks instead of copying (saves disk space)"
    )
    parser.add_argument(
        "--download-hf", action="store_true",
        help="Download full dataset from HuggingFace first, then filter"
    )
    parser.add_argument(
        "--hf-cache-dir", type=Path,
        default=Path("/root/autodl-tmp/data/.hf_cache"),
        help="Where to cache the full HuggingFace download"
    )
    return parser.parse_args()


def load_selected_ids(ids_file: Path) -> list[int]:
    """Load the 200 selected video IDs."""
    if not ids_file.exists():
        raise FileNotFoundError(f"IDs file not found: {ids_file}")
    ids = []
    for line in ids_file.read_text().strip().split("\n"):
        line = line.strip()
        if line and line.isdigit():
            ids.append(int(line))
    return ids


def copy_from_source(source_dir: Path, output_dir: Path, selected_ids: list[int],
                     use_symlink: bool = False) -> tuple[int, list[int]]:
    """Copy or symlink selected videos from a source directory."""
    output_dir.mkdir(parents=True, exist_ok=True)

    found = 0
    missing = []

    # Try multiple naming patterns: {id}.mp4, video_{id}.mp4, prompt_{id}.mp4, etc.
    for vid_id in selected_ids:
        dst = output_dir / f"{vid_id}.mp4"
        if dst.exists() or dst.is_symlink():
            found += 1
            continue

        # Try different possible filenames
        candidates = [
            source_dir / f"{vid_id}.mp4",
            source_dir / f"video_{vid_id}.mp4",
            source_dir / f"prompt_{vid_id}.mp4",
            source_dir / f"{vid_id:04d}.mp4",
        ]

        src = None
        for c in candidates:
            if c.exists():
                src = c
                break

        if src is None:
            missing.append(vid_id)
            continue

        if use_symlink:
            dst.symlink_to(src.resolve())
        else:
            shutil.copy2(src, dst)
        found += 1

    return found, missing


def download_from_huggingface(cache_dir: Path) -> Path:
    """
    Download the full MovieGenVideoBench dataset from HuggingFace.
    Returns the local path where the dataset was downloaded.
    """
    try:
        from huggingface_hub import snapshot_download
    except ImportError:
        print("ERROR: huggingface_hub not installed.")
        print("Run: pip install huggingface_hub")
        sys.exit(1)

    print("=" * 70)
    print("Downloading full MovieGenVideoBench from HuggingFace...")
    print("Repo: meta-ai-for-media-research/movie_gen_video_bench")
    print(f"Cache dir: {cache_dir}")
    print("=" * 70)
    print()

    cache_dir.mkdir(parents=True, exist_ok=True)

    local_dir = snapshot_download(
        repo_id="meta-ai-for-media-research/movie_gen_video_bench",
        repo_type="dataset",
        local_dir=str(cache_dir / "movie_gen_video_bench"),
    )

    local_path = Path(local_dir)
    print(f"\nDownloaded to: {local_path}")

    # List what was downloaded
    video_files = list(local_path.rglob("*.mp4"))
    print(f"Found {len(video_files)} video files in download")

    if video_files:
        print(f"Example files:")
        for f in video_files[:5]:
            print(f"  {f.relative_to(local_path)}")

    return local_path


def find_video_dir(download_path: Path) -> Path:
    """
    Find the directory containing video files within the downloaded dataset.
    The HuggingFace dataset structure may vary, so we search for mp4 files.
    """
    video_files = list(download_path.rglob("*.mp4"))
    if not video_files:
        print("ERROR: No .mp4 files found in downloaded dataset!")
        print(f"Downloaded path: {download_path}")
        print("Contents:")
        for item in sorted(download_path.iterdir()):
            print(f"  {item.name}")
        sys.exit(1)

    # Find the common parent directory of all video files
    parents = set(f.parent for f in video_files)
    if len(parents) == 1:
        return parents.pop()

    # If videos are in multiple dirs, return the download root
    return download_path


def main():
    args = parse_args()

    # Load selected IDs
    selected_ids = load_selected_ids(args.ids_file)
    print(f"Loaded {len(selected_ids)} selected video IDs")
    print(f"Output dir: {args.output_dir}")
    print()

    if args.download_hf:
        # Step 1: Download full dataset
        download_path = download_from_huggingface(args.hf_cache_dir)

        # Step 2: Find video directory
        video_dir = find_video_dir(download_path)
        print(f"\nVideo directory: {video_dir}")

        # Step 3: Filter to selected 200
        print(f"\nFiltering {len(selected_ids)} selected videos...")
        found, missing = copy_from_source(
            video_dir, args.output_dir, selected_ids, args.symlink
        )

        print(f"\nDone! {found}/{len(selected_ids)} videos copied to {args.output_dir}")
        if missing:
            print(f"\nWARNING: {len(missing)} videos not found.")
            print("This may be due to different naming conventions in the dataset.")
            print("Missing IDs (first 20):")
            for vid_id in missing[:20]:
                print(f"  - {vid_id}")
            if len(missing) > 20:
                print(f"  ... and {len(missing) - 20} more")
            print("\nPlease check the downloaded files and their naming pattern:")
            video_files = list(video_dir.rglob("*.mp4"))
            print(f"Available video files (first 10):")
            for f in sorted(video_files)[:10]:
                print(f"  {f.name}")

    elif args.source_dir:
        # Filter from local source
        print(f"Source: {args.source_dir}")
        print(f"Mode: {'symlink' if args.symlink else 'copy'}")
        print()

        if not args.source_dir.exists():
            print(f"ERROR: Source directory does not exist: {args.source_dir}")
            sys.exit(1)

        found, missing = copy_from_source(
            args.source_dir, args.output_dir, selected_ids, args.symlink
        )

        print(f"\nDone! {found}/{len(selected_ids)} videos prepared")
        if missing:
            print(f"WARNING: {len(missing)} videos not found in source:")
            for vid_id in missing[:20]:
                print(f"  - {vid_id}.mp4")
            if len(missing) > 20:
                print(f"  ... and {len(missing) - 20} more")

    else:
        print()
        print("=" * 70)
        print("USAGE: Download full dataset and filter to 200 selected videos")
        print("=" * 70)
        print()
        print("  python scripts/download_dataset.py --download-hf")
        print()
        print("Or if you already have the full dataset downloaded:")
        print()
        print("  python scripts/download_dataset.py --source-dir /path/to/videos")
        print()
        print("=" * 70)


if __name__ == "__main__":
    main()
